# stripping whitespace
favorite_lang = "python "
print(favorite_lang)
favorite_lang = favorite_lang.rstrip()
print(favorite_lang)
favorite_lang = " python"
favorite_lang = favorite_lang.lstrip()
print(favorite_lang)
favorite_lang = " python "
favorite_lang = favorite_lang.strip()
print(favorite_lang)
